<h3><b>Tentang Aplikasi:</b></h3>
<div align="justify">
	<p>Aplikasi Pohon Keputusan C4.5 untuk Prediksi Kelancaran Pembayaran Pinjaman Anggota Pada Primkoveri Bina Bakhti Pemalang</p> 
	<p>
		Bagian-bagian yang terdapat pada aplikasi ini adalah :
	</p>
	<p>1. Data Anggota : Berfungsi untuk Memasukkan data kasus anggota yang akan diproses menjadi pohon keputusan.</p>
	<p>2. Pohon Keputusan : Berfungsi untuk proses lakukan mining dan membentuk pohon keputusan dari data yang telah di masukkan sebelumnya.</p>
	<p>3. Proses Prediksi : Digunakan untuk melakukan prediksi kelancaran pembayaran terhadap anggota baru yang hendak melakukan peminjaman</p>				  
</div>